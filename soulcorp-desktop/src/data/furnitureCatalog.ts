const catalogById = new Map(FURNITURE_CATALOG.map((entry) => [entry.id, entry]));

export function getCatalogEntry(id: string): FurnitureCatalogEntry | undefined {
  return catalogById.get(id);
}

/** @deprecated Use getCatalogEntry */
export const catalogEntry = getCatalogEntry;

export function deskCatalogId(style: OfficeDeskStyle): string {
  const match = FURNITURE_CATALOG.find((entry) => entry.deskStyle === style);
  return match?.id ?? "desk_open";
}